# Code to demonstrate some of python's built-in functions
my_cars = ["BMW","Toyota", "Jeep", "Mercedes", "Honda"]

# 1: Enumerate function
print(list(enumerate(my_cars)))

# 2: Sort the list
my_cars.sort()
print(my_cars)

# 3: Length of object (my_car)
print(len(my_cars))

# 4: Power of a number
print(pow(25, 4))
